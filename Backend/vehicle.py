# Creating data model

class vehicle:
    def __init__(self, st_case, veh_no, numoccs, hit_run, owner, make, mod_year):
        self.st_case = st_case 
        self.veh_no = veh_no 
        self.numoccs = numoccs 
        self.hit_run = hit_run
        self.owner = owner 
        self.make = make
        self.mod_year = mod_year